
.MODEL SMALL
.STACK 100H
.DATA
FIRSTNUM DB 'ENTER THE FIRST DIGIT $'
SECONDNUM DB 'ENTER THE SECOND NUMBER $'
RESULT DB 'THE RESULT $'
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9H
    LEA DX,FIRSTNUM
    INT 21H
    
    MOV AH,1H
    INT 21H
    MOV BL,AL
    
    MOV AH,2H
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    MOV AH,9H
    LEA DX,SECONDNUM
    INT 21H
    
    MOV AH,1H
    INT 21H
    MOV BH,AL
    

    MOV AH,2H
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    MOV AH,9H
    LEA DX,RESULT
    INT 21H
    
    ADD BL,BH
    SUB BL,30H
    MOV AH,2H
    MOV DL,BL
    INT 21H
    
    EXIT:
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN
    
