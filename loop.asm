
.MODEL SMALL
.STACK 100H
.DATA 
A DB 'LOOP CONCEPT $' 
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9h
    LEA DX,A
    INT 21H
    
    MOV AH,2H
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    MOV CX,1AH
    MOV AH,2H
    MOV DL,'A'
    
    LEVEL1:
    INT 21H
    INC DL
    LOOP LEVEL1
    
    EXIT:
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN
    
